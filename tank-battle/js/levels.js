// 把 13x13 大格布局展开成 26x26 小方块地图，并加上基地保护墙
function buildMap(levelIndex) {
  const cfg = LEVELS[(levelIndex - 1) % LEVELS.length];
  const map = [];
  for (let r = 0; r < MAP_TILES; r++) map.push(new Array(MAP_TILES).fill(TILE_EMPTY));
  for (let cr = 0; cr < 13; cr++) {
    for (let cc = 0; cc < 13; cc++) {
      const ch = cfg.layout[cr][cc];
      const t = ch === '#' ? TILE_BRICK
              : ch === '@' ? TILE_STEEL
              : ch === '~' ? TILE_WATER
              : ch === '%' ? TILE_TREE
              : TILE_EMPTY;
      if (t !== TILE_EMPTY) {
        map[cr * 2][cc * 2] = t;
        map[cr * 2][cc * 2 + 1] = t;
        map[cr * 2 + 1][cc * 2] = t;
        map[cr * 2 + 1][cc * 2 + 1] = t;
      }
    }
  }
  // 基地周围的砖墙(经典布局)
  const fort = [[11, 5], [11, 6], [11, 7], [12, 5], [12, 7]];
  for (const [cr, cc] of fort) {
    map[cr * 2][cc * 2] = TILE_BRICK;
    map[cr * 2][cc * 2 + 1] = TILE_BRICK;
    map[cr * 2 + 1][cc * 2] = TILE_BRICK;
    map[cr * 2 + 1][cc * 2 + 1] = TILE_BRICK;
  }
  return map;
}
